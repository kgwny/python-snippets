# print文でセパレータ（区切り文字）を指定することができる

# 区切り文字なしの場合
print(100, 200, 300)
# 100 200 300

print(100, 200, 300, sep = ',')
# 100,200,300

print(100, 200, 300, sep = ', ')
# 100, 200, 300

print(100, 200, 300, sep = ' | ')
# 100 | 200 | 300

# タブ
print(100, 200, 300, sep = '\t')
# 100     200     300

# 改行
print(100, 200, 300, sep = '\n')
# 100
# 200
# 300

# 詰める場合は区切り文字を空文字にする
print(100, 200, 300, sep = '')
# 100200300
