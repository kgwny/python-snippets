
# int 整数
integer = 0

print(type(integer))
# <class 'int'>
print(integer)
# 0


# 2進数:先頭に0bを付ける
val = 0b10
print(type(val))
# <class 'int'>
print(val)
# 2

# 8進数:先頭に0oを付ける
val = 0o10
print(type(val))
# <class 'int'>
print(val)
# 8

# 16進数:先頭に0xを付ける
val = 0x10
print(type(val))
# <class 'int'>
print(val)
# 16
