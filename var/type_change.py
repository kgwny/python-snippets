
# str to int
var = int("1") + int("-3")
print(type(var))
# <class 'int'>
print(var)
# -2

# abs関数:絶対値
print(abs(var))
# 2.0

# int to str
string = str(var)
print(type(string))
# <class 'str'>
print(string)
# 2.0

# float + int = float
var = float("1.0") + int("1")
print(type(var))
# <class 'float'>
print(var)
# 2.0

# float to int
integer = int(var)
print(type(integer))
# <class 'int'>
print(integer)
# 2
